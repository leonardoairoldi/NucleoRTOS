/* USER CODE END Header_Default_task */
void Default_task(void const * argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
	for(uint8_t i = 0; i < 144; i++) {
		vCells[i] = rand() % 3000 + 2000;
		tCells[i] = rand() % 6000;
	}
	current = rand() % 1000;
	HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
    osDelay(300);
  }
  /* USER CODE END 5 */
}

